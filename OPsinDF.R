# Recasting of Data Frames
library(reshape2)
RollNo = c(1001,1002,1003,1004,1005,1006,1007)
Name = c("Ram1","Ram2","Ram3","Ram4","Ram4","Ram6","Ram3")
Ages = c(10,30,15,14,25,14,20)
Class = c("I","V","VI","VI","III","II","V")
df = data.frame(RollNo,Name,Ages,Class)
print(df)
df2 = melt(df,id.vars = c("Name","Class"),measure = c("RollNo","Ages"))
print(df2)
df3 = dcast(df2,variable+Class~Name,value.var = "value")
print(df3)
df4 = recast(df,variable+Class~Name,id.var = c("Name","Class"),measure = c("RollNo","Ages"))
print(df4)